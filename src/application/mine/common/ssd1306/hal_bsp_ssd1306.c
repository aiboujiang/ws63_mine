void SSD1306_SetPos(unsigned char x, unsigned char y) // 设置起始点坐标
{
    uint8_t illegal_keyword = 4;  // 右移4位

    if (!g_ssd1306_operational) {
        return;
    }

    if (SSD1306_WriteCmd(0xb0 + y) != ERRCODE_SUCC) {
        return;
    }
    if (SSD1306_WriteCmd(((x & 0xf0) >> illegal_keyword) | 0x10) != ERRCODE_SUCC) {
        return;
    }
    (void)SSD1306_WriteCmd((x & 0x0f) | 0x01);
}

void SSD1306_Fill(unsigned char fill_Data) // 全屏填充
{
    unsigned char m, n;
    uint8_t number_of_rows = 8;
    uint8_t number_of_columns = 128;

    if (!g_ssd1306_operational) {
        return;
    }

    for (m = 0; m < number_of_rows; m++) {
        if (SSD1306_WriteCmd(0xb0 + m) != ERRCODE_SUCC) { // page0-page1
            return;
        }
        if (SSD1306_WriteCmd(0x00) != ERRCODE_SUCC) {     // low column start address
            return;
        }
        if (SSD1306_WriteCmd(0x10) != ERRCODE_SUCC) {     // high column start address
            return;
        }
        for (n = 0; n < number_of_columns; n++) {
            if (SSD1306_WiteData(fill_Data) != ERRCODE_SUCC) {
                return;
            }
        }
    }
}

void SSD1306_CLS(void) // 清屏
{
    SSD1306_Fill(0x00);
}

// --------------------------------------------------------------
// Prototype      : void OLED_ON(void)
// Parameters     : none
// Description    : 将OLED从休眠中唤醒
// --------------------------------------------------------------
void SSD1306_ON(void)
{
    if (!g_ssd1306_operational) {
        return;
    }

    if (SSD1306_WriteCmd(0X8D) != ERRCODE_SUCC) { // 设置电荷泵
        return;
    }
    if (SSD1306_WriteCmd(0X14) != ERRCODE_SUCC) { // 开启电荷泵
        return;
    }
    (void)SSD1306_WriteCmd(0XAF); // OLED唤醒
}

// --------------------------------------------------------------
// Prototype      : void OLED_OFF(void)
// Parameters     : none
// Description    : 让OLED休眠 -- 休眠模式下,OLED功耗不到10uA
// --------------------------------------------------------------
void SSD1306_OFF(void)
{
    if (!g_ssd1306_operational) {
        return;
    }

    if (SSD1306_WriteCmd(0X8D) != ERRCODE_SUCC) { // 设置电荷泵
        return;
    }
    if (SSD1306_WriteCmd(0X10) != ERRCODE_SUCC) { // 关闭电荷泵
        return;
    }
    (void)SSD1306_WriteCmd(0XAE); // OLED休眠
}

/**
 * @brief  显示字符串
 */
void SSD1306_ShowStr(uint8_t x, uint8_t y, char* ch, uint8_t  TextSize)

{
    unsigned char c = 0, i = 0, j = 0;
    uint8_t number_of_letter_lattice = 6;
    uint8_t pos_x = x;
    uint8_t pos_y = y;

    if ((!g_ssd1306_operational) || (ch == NULL)) {
        return;
    }

    switch (TextSize) {
        case TEXT_SIZE_8:
            while (ch[j] != '\0') {
                c = ch[j] - ' ';
                if (pos_x > TEXT_8_MAX_ROW_POS) {
                    pos_x = 0;
                    pos_y++;
                }
                SSD1306_SetPos(pos_x, pos_y);
                for (i = 0; i < number_of_letter_lattice; i++) {
                    if (SSD1306_WiteData(F6x8[c][i]) != ERRCODE_SUCC) {
                        return;
                    }
                }
                pos_x += TEXT_SIZE_8_WIDTH_6;
                j++;
            }
            break;

        case TEXT_SIZE_16:
            pos_y *= OFFSET_2;
            while (ch[j] != '\0') {
                c = ch[j] - ' ';
                if (pos_x > TEXT_16_MAX_ROW_POS) {
                    pos_x = 0;
                    pos_y++;
                }
                SSD1306_SetPos(pos_x, pos_y);
                for (i = 0; i < TEXT_SIZE_16_WIDTH_8; i++) {
                    if (SSD1306_WiteData(F8X16[c * OFFSET_16 + i]) != ERRCODE_SUCC) {
                        return;
                    }
                }
                SSD1306_SetPos(pos_x, pos_y + 1);

                for (i = 0; i < TEXT_SIZE_16_WIDTH_8; i++) {
                    if (SSD1306_WiteData(F8X16[c * OFFSET_16 + i + OFFSET_8]) != ERRCODE_SUCC) {
                        return;
                    }
                }
                pos_x += TEXT_SIZE_16_WIDTH_8;
                j++;
            }
            break;

        default:
            break;
    }
}

/**
 * @brief  显示一张图片
 * @note   取模方式： 阳码、列行式、逆向、十六进制数、输出索引格、C51格式
 * @param  xMove: x坐标
 * @param  yMove: y坐标
 * @param  width: 宽
 * @param  height: 高
 */
void SSD1306_DrawBMP(unsigned char xMove, unsigned char yMove,
                     unsigned char width, unsigned char height,
                     unsigned char *BMP)
{
    unsigned short j = 0, x = 0, y = 0;
    unsigned char x0 = xMove;
    unsigned char y0 = yMove / 8;
    unsigned char x1 = xMove + width;
    unsigned char y1 = height / 8 + y0;

    if ((!g_ssd1306_operational) || (BMP == NULL)) {
        return;
    }

    for (y = y0; y < y1; y++) {
        SSD1306_SetPos(x0, y);
        for (x = x0; x < x1; x++) {
            if (SSD1306_WiteData(BMP[j]) != ERRCODE_SUCC) {
                return;
            }
            j++;
        }
    }
}