static bool g_ssd1306_operational = false;
static bool g_ssd1306_write_fault_latched = false;
static uint32_t g_ssd1306_last_err = ERRCODE_SUCC;

static uint32_t SSD1306_SendData(uint8_t *buff, size_t size)
{
    uint16_t dev_addr = SSD1306_I2C_ADDR;
    i2c_data_t data = {0};

    if ((buff == NULL) || (size == 0)) {
        return ERRCODE_FAIL;
    }

    if (g_ssd1306_write_fault_latched) {
        return g_ssd1306_last_err;
    }

    data.send_buf = buff;
    data.send_len = size;
    uint32_t ret = uapi_i2c_master_write(SSD1306_I2C_IDX, dev_addr, &data);
    if (ret != 0) {
        g_ssd1306_write_fault_latched = true;
        g_ssd1306_last_err = ret;
        g_ssd1306_operational = false;
        printf("oled(%02X) failed, %0X!\n", data.send_buf[1], ret);
        return ret;
    }
    return ret;
}

// 写命令
static uint32_t SSD1306_WriteCmd(uint8_t byte)
{
    uint8_t buffer[] = {0x00, byte};
    return SSD1306_SendData(buffer, sizeof(buffer));
}
// 写数据
static uint32_t SSD1306_WiteData(uint8_t byte)
{
    uint8_t buffer[] = {0x40, byte};
    return SSD1306_SendData(buffer, sizeof(buffer));
}

uint32_t SSD1306_Init(void)
{
    uint32_t result;
    uint32_t baudrate = SSD1306_I2C_SPEED;
    uint32_t hscode = I2C_MASTER_ADDR;
    uint32_t i;
    static const uint8_t init_cmds[] = {
        0xAE, 0x20, 0x10, 0xB0, 0xC8, 0x00, 0x10, 0x40,
        0x81, 0xFF, 0xA1, 0xA6, 0xA8, 0x3F, 0xA4, 0xD3,
        0x00, 0xD5, 0xF0, 0xD9, 0x22, 0xDA, 0x12, 0xDB,
        0x20, 0x8D, 0x14, 0xAF, 0xAF
    };

    g_ssd1306_operational = false;
    g_ssd1306_write_fault_latched = false;
    g_ssd1306_last_err = ERRCODE_SUCC;

    /* I2C pinmux. */
    uapi_pin_set_mode(I2C_SCL_MASTER_PIN, CONFIG_PIN_MODE);
    uapi_pin_set_mode(I2C_SDA_MASTER_PIN, CONFIG_PIN_MODE);       
    uapi_pin_set_pull(I2C_SCL_MASTER_PIN, PIN_PULL_TYPE_UP);
    uapi_pin_set_pull(I2C_SDA_MASTER_PIN, PIN_PULL_TYPE_UP);
    result =uapi_i2c_master_init(1, baudrate, hscode);
    if (result != ERRCODE_SUCC) {
        printf("I2C SSD1306 Init status is 0x%x!!!\r\n", result);
        return result;
    }
    else{
         printf("I2C SSD1306 Init succ!\r\n");
    }

    for (i = 0; i < sizeof(init_cmds); i++) {
        result = SSD1306_WriteCmd(init_cmds[i]);
        if (result != ERRCODE_SUCC) {
            printf("I2C SSD1306 init cmd failed, ret=0x%x\r\n", result);
            return result;
        }
    }

    g_ssd1306_operational = true;
    return ERRCODE_SUCC;
}
